const BADGE_MAP: Record<string, { bg: string; color: string }> = {
  'Гурманам':    { bg: 'rgba(255,92,40,0.15)', color: 'var(--accent)' },
  'Ресторанам':  { bg: 'rgba(57,255,209,0.15)', color: 'var(--teal)' },
  'Событие':     { bg: 'rgba(160,120,255,0.15)', color: '#a078ff' },
  'Акция':       { bg: 'rgba(255,200,40,0.15)', color: '#ddb620' },
  'Новость':     { bg: 'rgba(80,160,255,0.15)', color: '#50a0ff' },
};

async function getArticle(slug: string) {
  try {
    const backendUrl = process.env.BACKEND_URL || 'http://localhost:3001';
    const res = await fetch(`${backendUrl}/api/articles/${slug}`, {
      next: { revalidate: 300 },
    });
    if (res.ok) return res.json();
  } catch {}

  const staticArticle = STATIC_ARTICLES[slug];
  if (staticArticle) return { ...staticArticle, slug };

  return null;
}

export async function generateMetadata({ params }: { params: { slug: string } }): Promise<Metadata> {
  const article = await getArticle(params.slug);
  if (!article) return { title: 'Статья не найдена' };
  return {
    title: `${article.title} — Menu-Rest`,
    description: article.excerpt || '',
    openGraph: {
      title: article.title,
      description: article.excerpt || '',
    },
  };
}

export default async function ArticlePage({ params }: { params: { slug: string } }) {
  const article = await getArticle(params.slug);
  if (!article) notFound();

  const formatDate = (date: string | null) => {
    if (!date) return '';
    return new Date(date).toLocaleDateString('ru-RU', { day: 'numeric', month: 'long', year: 'numeric' });
  };

  const badge = BADGE_MAP[article.category] || BADGE_MAP['Новость'];

  return (
    <div className="max-w-[800px] mx-auto px-6 py-12">
      {/* Breadcrumbs */}
      <nav className="flex items-center gap-2 text-[12px] text-[var(--text3)] mb-8">
        <Link href="/" className="no-underline text-[var(--text3)] hover:text-[var(--accent)]">Главная</Link>
        <span>/</span>
        <Link href="/blog" className="no-underline text-[var(--text3)] hover:text-[var(--accent)]">Журнал</Link>
        <span>/</span>
        <span className="text-[var(--text2)] line-clamp-1">{article.title}</span>
      </nav>

      {/* Category badge */}
      {article.category && (
        <span
          className="inline-block px-3 py-1 rounded-full text-[11px] font-semibold mb-5"
          style={{ background: badge.bg, color: badge.color }}>
          {article.category}
        </span>
      )}

      {/* Cover */}
      <div className="rounded-[20px] overflow-hidden mb-8 h-[350px] relative">
        {article.coverUrl ? (
          <img src={article.coverUrl} alt={article.title} className="w-full h-full object-cover" />
        ) : (
          <ArticleCover slug={params.slug} title={article.title} className="w-full h-full" />
        )}
      </div>

      {/* Title */}
      <h1 className="font-serif text-[36px] font-bold text-[var(--text)] mb-3 leading-tight">{article.title}</h1>

      {article.publishedAt && (
        <p className="text-[13px] text-[var(--text3)] mb-8">{formatDate(article.publishedAt)}</p>
      )}

      {/* Body */}
      <div
        className="prose prose-invert max-w-none text-[var(--text2)] text-[15px] leading-[1.8] [&_h2]:font-serif [&_h2]:text-[22px] [&_h2]:font-bold [&_h2]:text-[var(--text)] [&_h2]:mt-8 [&_h2]:mb-3 [&_p]:mb-4 [&_ul]:mb-4 [&_ul]:pl-5 [&_li]:mb-2 [&_li]:text-[var(--text2)]"
        dangerouslySetInnerHTML={{ __html: article.body }}
      />

      {/* Related restaurants */}
      {article.restaurants?.length > 0 && (
        <div className="mt-12 pt-8 border-t" style={{ borderColor: 'var(--card-border)' }}>
          <h2 className="text-[20px] font-semibold text-[var(--text)] mb-4">Упоминаемые рестораны</h2>
          <div className="flex flex-wrap gap-3">
            {article.restaurants.map((r: { id: number; slug: string; name: string }) => (
              <Link key={r.id} href={`/restaurants/${r.slug}`}
                className="px-4 py-2.5 rounded-full text-[13px] font-semibold border no-underline transition-all"
                style={{ background: 'var(--glass)', color: 'var(--text2)', borderColor: 'var(--glass-border)' }}>
                {r.name}
              </Link>
            ))}
          </div>
        </div>
      )}

      {/* Back */}
      <div className="mt-12">
        <Link href="/blog" className="text-[13px] text-[var(--accent)] no-underline font-semibold">← Все материалы</Link>
      </div>
    </div>
  );
}
