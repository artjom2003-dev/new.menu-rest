# Compile patterns: sort by length (longest first) for best match
_SORTED_KEYS = sorted(KBZHU_LOOKUP.keys(), key=len, reverse=True)
_PATTERNS = [(key, re.compile(re.escape(key), re.IGNORECASE)) for key in _SORTED_KEYS]


def _match_dish(name: str) -> Optional[Tuple[str, int, float, float, float]]:
    """
    Find best KBZHU match for a dish name.
    Returns (matched_key, calories, protein, fat, carbs) or None.
    """
    name_lower = name.lower()
    for key, pattern in _PATTERNS:
        if pattern.search(name_lower):
            cal, prot, fat, carbs = KBZHU_LOOKUP[key]
            return (key, cal, prot, fat, carbs)
    return None


def run_kbzhu_estimator():
    """Estimate KBZHU for dishes with missing nutrition data."""
    print("[KBZHU] Запуск оценки КБЖУ для блюд...")
    log_import('enrichment', 'kbzhu_estimator', 'started')

    conn = get_connection()
    cur = conn.cursor()

    # Add kbzhu_source column if not exists
    try:
        cur.execute("ALTER TABLE dishes ADD COLUMN kbzhu_source TEXT")
        conn.commit()
        print("[KBZHU] Добавлена колонка kbzhu_source")
    except sqlite3.OperationalError:
        pass  # column already exists

    # Fetch dishes where ALL KBZHU fields are NULL
    rows = cur.execute("""
        SELECT id, name, composition
        FROM dishes
        WHERE calories IS NULL
          AND protein IS NULL
          AND fat IS NULL
          AND carbs IS NULL
    """).fetchall()

    total = len(rows)
    matched = 0
    unmatched = 0
    unmatched_examples = []

    for row in rows:
        dish_id = row['id']
        dish_name = row['name'] or ''

        result = _match_dish(dish_name)
        if result:
            key, cal, prot, fat, carbs = result
            cur.execute("""
                UPDATE dishes
                SET calories = ?, protein = ?, fat = ?, carbs = ?,
                    kbzhu_source = 'estimated'
                WHERE id = ?
            """, (cal, prot, fat, carbs, dish_id))
            matched += 1
        else:
            unmatched += 1
            if len(unmatched_examples) < 15:
                unmatched_examples.append(dish_name)

    # Mark existing exact values
    cur.execute("""
        UPDATE dishes
        SET kbzhu_source = 'exact'
        WHERE (calories IS NOT NULL OR protein IS NOT NULL
               OR fat IS NOT NULL OR carbs IS NOT NULL)
          AND kbzhu_source IS NULL
    """)

    conn.commit()

    already_filled = cur.execute("""
        SELECT COUNT(*) FROM dishes
        WHERE kbzhu_source = 'exact'
    """).fetchone()[0]

    conn.close()

    print(f"[KBZHU] Результаты:")
    print(f"  Всего блюд без КБЖУ: {total}")
    print(f"  Сопоставлено (estimated): {matched}")
    print(f"  Не сопоставлено: {unmatched}")
    print(f"  Уже имели данные (exact): {already_filled}")
    if unmatched_examples:
        print(f"  Примеры несопоставленных:")
        for ex in unmatched_examples:
            print(f"    - {ex}")

    log_import('enrichment', 'kbzhu_estimator', 'completed', matched)
    print("[KBZHU] Готово.")


if __name__ == '__main__':
    run_kbzhu_estimator()
